class ExplicitTypeCasting{
    public static void main(String args[]){
        double a = 45.5;
        int b;
        b = (int)a;
        System.out.println(b);



        int n1 = 12; //we are use integer type data type so that when we performe permutation and combinataion between 2 intger result always integer 
        int n2 = 5;
        float res = n1/n2;
        System.out.println(res);

        float num1 = 45;
        float num2 = 8;

        double result = num1 / num2;
        
        System.out.println(result);




    }
}