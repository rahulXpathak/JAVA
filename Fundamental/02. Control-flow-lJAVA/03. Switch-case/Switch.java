class Switch{
    public static void main(String args[]){
        int num = 100;

        switch (num) {
            case 100 : System.out.println("Case 1");
            break;
            case 200 : System.out.println("Case 2");
            break;
            case 300 : System.out.println("Case 3");
            break;
        }
    }
}