class arithmetic{
    public static void main(String args[]){
        int num1 = 20;
        int num2 = 25;

        System.out.println(num1 + num2);
        System.out.println(num1 - num2);
        System.out.println(num1 * num2);
        System.out.println(num1 / num2);
        System.out.println(num1 % num2);
    }
}