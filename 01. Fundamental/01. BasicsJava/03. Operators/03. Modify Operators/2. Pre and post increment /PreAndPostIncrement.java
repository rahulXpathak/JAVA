class PreAndPostIncrement{
    public static void main (String args[]){
        int a = 5;
        int b = a++; // post increment 
        System.out.println(a);
        System.out.println(b);


        int num1 = 5;
        int num2 = ++num1; //pre increment 
        System.out.println(num1);
        System.out.println(num2);
        
    }
}