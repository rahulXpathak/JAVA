class PreAndPostDecrement{
    public static void main(String args[]){
        //pre decrement :- 
        int a = 5;
        int b = --a;
        System.out.println(a);
        System.out.println(b);

        //post increment :- 
        int num1 = 5;
        int num2 = num1--;
        System.out.println(num1);
        System.out.println(num2);
    }
}